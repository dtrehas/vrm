//IsReservedWord finds if a word needs to be escaped with double quotes even it is not needed in PG but in SQL:xxxx versions
func IsReservedWord(word string) bool {
	capitalisedWord := strings.ToUpper(word)
	return ReservedWords[capitalisedWord]
}

//EscapeIfReservedWord escapes a word with double quotes even it is not needed in PG but in SQL:xxxx versions
func EscapeIfReservedWord(word string) string {
	if IsReservedWord(word) {
		word = `"` + word + `"`
	}
	return word
}
